const createAditivos = (contratoId: string, count: number): Aditivo[] => {
  const aditivos: Aditivo[] = [];
  for (let i = 1; i <= count; i++) {
    aditivos.push({
      id: `aditivo-${contratoId}-${i}`,
      contratoId,
      tipo: i % 2 === 0 ? "valor" : "prazo",
      valorAdicional: Math.floor(Math.random() * 500000) + 100000,
      novoPrazoDias: i % 2 === 0 ? 90 : 180,
      justificativa:
        i % 2 === 0
          ? "Reajuste de preços de materiais"
          : "Necessidade de adequação técnica",
      dataAprovacao: new Date(`2024-0${i + 3}-15`),
      numero: `${i}/2024`,
    });
  }
  return aditivos;
};

